i18n.getMessage = function (messageId, options) {
    options = options || {};
    var result = messageId;

    //==== MyChange begin =========
    // Получим сообщения в зависимости от текущего языка, выбранного пользователем
    if (!this.language) {
        this.language = (lb.getMsg('languageSite')) ? lb.getMsg('languageSite') : null;
        if (!this.language) {
            this.language = window.navigator.userLanguage || window.navigator.language;
        }
    }
    var languageCd = this.language.substring(0, 2).toUpperCase();
    //==== MyChange end =========


    try {
        var messages = this[languageCd] || this['EN'];
        if (messages[messageId]) {
            var template = new Template(messages[messageId]);
            return template.evaluate(options);
        }
    } catch (e) {
        result = messageId;
    }
    return result;
};
